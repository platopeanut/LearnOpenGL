// process all input: query GLFW whether relevant keys are pressed/released this frame and react accordingly
// ---------------------------------------------------------------------------------------------------------
    void processInput(GLFWwindow *window)
    {
        if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
            glfwSetWindowShouldClose(window, true);

        auto cameraSpeed = static_cast<float>(2.5 * deltaTime);
        if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
            cameraPos += cameraSpeed * cameraFront;
        if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
            cameraPos -= cameraSpeed * cameraFront;
        if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
            cameraPos -= glm::normalize(glm::cross(cameraFront, cameraUp)) * cameraSpeed;
        if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
            cameraPos += glm::normalize(glm::cross(cameraFront, cameraUp)) * cameraSpeed;
    }

    // glfw: whenever the mouse moves, this callback is called
// -------------------------------------------------------
    void mouse_callback(GLFWwindow* window, double x_posIn, double y_posIn)
    {
        auto x_pos = static_cast<float>(x_posIn);
        auto y_pos = static_cast<float>(y_posIn);

        if (firstMouse)
        {
            lastX = x_pos;
            lastY = y_pos;
            firstMouse = false;
        }

        float x_offset = x_pos - lastX;
        float y_offset = lastY - y_pos; // reversed since y-coordinates go from bottom to top
        lastX = x_pos;
        lastY = y_pos;

        float sensitivity = 0.1f; // change this value to your liking
        x_offset *= sensitivity;
        y_offset *= sensitivity;

        yaw += x_offset;
        pitch += y_offset;

        // make sure that when pitch is out of bounds, screen doesn't get flipped
        if (pitch > 89.0f)
            pitch = 89.0f;
        if (pitch < -89.0f)
            pitch = -89.0f;

        glm::vec3 front;
        front.x = (float) (cos(glm::radians(yaw)) * cos(glm::radians(pitch)));
        front.y = (float) sin(glm::radians(pitch));
        front.z = (float) (sin(glm::radians(yaw)) * cos(glm::radians(pitch)));
        cameraFront = glm::normalize(front);
    }

// glfw: whenever the mouse scroll wheel scrolls, this callback is called
// ----------------------------------------------------------------------
    void scroll_callback(GLFWwindow* window, double x_offset, double y_offset)
    {
        fov -= (float)y_offset;
        if (fov < 1.0f)
            fov = 1.0f;
        if (fov > 45.0f)
            fov = 45.0f;
    }
